"""Phase 3 — Keyframe Image Generation.

Produces N+1 keyframe images for N clips:

  Frame 0      : Initial character reference — generated by Imagen 3 from text.
                 This is the anchor face. All subsequent frames are edits of this.

  Frames 1..N  : Expression-transition frames — generated by Gemini Flash Image.
                 Each one takes the previous frame and changes ONLY the expression
                 to match the end_emotion of that clip. This creates seamless
                 clip boundaries: last pixel of clip i = first pixel of clip i+1.

WHY TWO MODELS:
  - Imagen 3 excels at generating photorealistic humans from text. It produces
    the highest quality initial character reference.
  - Gemini Flash Image excels at image editing with an input image. It can
    change only the expression while preserving face, outfit, background exactly.
  - Using Imagen for editing is not supported (no input image parameter).
  - Using Gemini text-to-image for Frame 0 works but quality is lower than Imagen.

FALLBACK CHAIN for Frame 0:
  1. Try Imagen 3 (best quality)
  2. If Imagen fails → try Gemini Flash Image text-to-image (acceptable quality)
  3. If both fail → raise RuntimeError (cannot continue without anchor frame)

FALLBACK for Frames 1..N:
  If Gemini Image edit fails → reuse previous frame (seamless cut still works,
  just expression won't change at that boundary).
"""

import base64
import logging
from typing import Callable, Optional

from .. import config
from ..ai.imagen_client import imagen_client
from ..ai.gemini_client import gemini_image_client
from ..models import ClipPrompt, KeyFrame, ProductionBrief
from ..prompts.system_imager import build_imagen_prompt, SYSTEM_IMAGER

logger = logging.getLogger(__name__)


